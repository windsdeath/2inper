#!/usr/bin/env node
'use strict';

const fs = require('fs');
const path = require('path');

// js-yaml 로드 (npm install 후 사용 가능)
let yaml;
try {
  yaml = require('js-yaml');
} catch {
  console.error('[graphify] js-yaml 없음. "npm install" 먼저 실행하세요.');
  process.exit(1);
}

// ─── 경로 상수 ────────────────────────────────────────────────────────────────

const VAULT_ROOT = path.resolve(__dirname, '../..');
const GRAPH_DIR  = path.join(VAULT_ROOT, 'graph');

// graphify가 직접 생성하는 경로 (소스 스캔에서 제외)
const AUTO_GENERATED_PREFIXES = ['graph/', 'wiki/communities/', 'wiki/entities/'];

const WIKILINK_RE = /\[\[([^\]|#\n]+?)(?:\|[^\]\n]+)?\]\]/g;

// ─── 파일 스캔 ────────────────────────────────────────────────────────────────

function isAutoGenerated(relPath) {
  const normalized = relPath.replace(/\\/g, '/');
  return AUTO_GENERATED_PREFIXES.some(p => normalized.startsWith(p));
}

function collectMarkdownFiles(dir, vault, acc = []) {
  let entries;
  try {
    entries = fs.readdirSync(dir, { withFileTypes: true });
  } catch {
    return acc;
  }
  for (const entry of entries) {
    if (entry.name.startsWith('.') || entry.name === 'node_modules') continue;
    const full = path.join(dir, entry.name);
    const rel  = path.relative(vault, full).replace(/\\/g, '/');
    if (entry.isDirectory()) {
      collectMarkdownFiles(full, vault, acc);
    } else if (entry.name.endsWith('.md')) {
      acc.push({ full, rel });
    }
  }
  return acc;
}

function parseMarkdownFile({ full, rel }) {
  const raw = fs.readFileSync(full, 'utf8');

  let frontmatter = {};
  let body        = raw;

  if (raw.startsWith('---\n') || raw.startsWith('---\r\n')) {
    const end = raw.indexOf('\n---', 4);
    if (end !== -1) {
      try {
        frontmatter = yaml.load(raw.slice(4, end)) || {};
      } catch {
        frontmatter = {};
      }
      body = raw.slice(end + 4).trimStart();
    }
  }

  // wikilink 추출 (전체 raw에서)
  const links = new Set();
  WIKILINK_RE.lastIndex = 0;
  let m;
  while ((m = WIKILINK_RE.exec(raw)) !== null) {
    links.add(m[1].trim());
  }

  return { rel, frontmatter, body, links: [...links] };
}

// ─── 그래프 빌드 ──────────────────────────────────────────────────────────────

function resolveLink(link, nodes, _fromKey) {
  const noExt = link.replace(/\.md$/, '');
  if (nodes.has(noExt)) return noExt;

  // 파일명(마지막 세그먼트)으로 매칭
  const target = noExt.split('/').pop();
  for (const key of nodes.keys()) {
    if (key.split('/').pop() === target) return key;
  }
  return null;
}

function buildGraph(vault) {
  const files    = collectMarkdownFiles(vault, vault);
  const nodes    = new Map(); // key(relPath without .md) → parsed

  for (const f of files) {
    const parsed = parseMarkdownFile(f);
    const key    = f.rel.replace(/\.md$/, '');
    nodes.set(key, parsed);
  }

  const edges    = [];
  const frontier = new Set();

  for (const [src, node] of nodes) {
    if (isAutoGenerated(src + '.md')) continue;
    for (const link of node.links) {
      const resolved = resolveLink(link, nodes, src);
      if (resolved) {
        edges.push({ from: src, to: resolved });
      } else {
        frontier.add(link);
      }
    }
  }

  return { nodes, edges, frontier };
}

// ─── Audit 리포트 생성 ────────────────────────────────────────────────────────

function generateAudit(graph) {
  const { nodes, edges, frontier } = graph;

  const inDeg  = new Map([...nodes.keys()].map(k => [k, 0]));
  const outDeg = new Map([...nodes.keys()].map(k => [k, 0]));
  for (const { from, to } of edges) {
    outDeg.set(from, (outDeg.get(from) || 0) + 1);
    inDeg.set(to,   (inDeg.get(to)    || 0) + 1);
  }

  const orphans = [];
  for (const [key] of nodes) {
    if (isAutoGenerated(key + '.md')) continue;
    if ((inDeg.get(key) || 0) === 0 && (outDeg.get(key) || 0) === 0) {
      orphans.push(key);
    }
  }

  const stale = [];
  const now   = Date.now();
  for (const [key, node] of nodes) {
    if (isAutoGenerated(key + '.md')) continue;
    const lr = node.frontmatter.last_reviewed;
    if (lr) {
      const d = new Date(lr);
      if (!isNaN(d) && (now - d.getTime()) / 86_400_000 > 180) stale.push(key);
    }
  }

  const today = new Date().toISOString().split('T')[0];
  const frontierList = [...frontier]
    .filter(f => !f.startsWith('http'))
    .slice(0, 50);

  return `---
type: audit
status: mature
confidence: high
owner: knowledge-manager
tags: [graphify, audit, auto-generated]
created: ${today}
last_reviewed: ${today}
---

# Graph Audit Report

Generated: ${today}

## Summary

| 항목 | 개수 |
|------|------|
| 전체 노드 | ${nodes.size} |
| 전체 엣지 | ${edges.length} |
| Orphan | ${orphans.length} |
| Frontier (미생성 링크) | ${frontierList.length} |
| Stale (6개월 초과) | ${stale.length} |

## Orphan 노드

다른 노드와 연결 없는 고립 노드. raw에 보강 노트 추가 권장.

${orphans.length === 0 ? '_없음_' : orphans.map(o => `- \`${o}\``).join('\n')}

## Frontier (위키 갭)

참조되지만 파일이 없는 링크 — LLM이 채울 수 있는 지점.

${frontierList.length === 0 ? '_없음_' : frontierList.map(f => `- \`${f}\``).join('\n')}

## Stale 노드

\`last_reviewed\`가 6개월(180일) 초과인 페이지.

${stale.length === 0 ? '_없음_' : stale.map(s => `- \`${s}\``).join('\n')}

## Contradiction

자동 감지 미지원 — Knowledge Manager가 수동 검토 필요.
`;
}

// ─── Graph JSON 생성 ──────────────────────────────────────────────────────────

function generateGraphJson(graph) {
  const { nodes, edges } = graph;
  const nodeList = [...nodes.entries()].map(([id, n]) => ({
    id,
    type:          n.frontmatter.type          || 'unknown',
    status:        n.frontmatter.status        || 'unknown',
    owner:         n.frontmatter.owner         || null,
    tags:          n.frontmatter.tags          || [],
    last_reviewed: n.frontmatter.last_reviewed || null,
  }));
  return JSON.stringify({ generated: new Date().toISOString(), nodes: nodeList, edges }, null, 2);
}

